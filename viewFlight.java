    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ams", "root", "");

    // Assume 'destinationField' is a JTextField
    String destination = destinationField.getText().trim();
    String sql = "SELECT * FROM manageflight";
    if (!destination.isEmpty()) {
        sql += " WHERE destination = ?";  // Filter results based on destination
    }

    PreparedStatement ptst = con.prepareStatement(sql);
    if (!destination.isEmpty()) {
        ptst.setString(1, destination);  // Set the destination parameter in the SQL query
    }

    ResultSet rs = ptst.executeQuery();
    DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
    dt.setRowCount(0);

    while (rs.next()) {
        Object o[] = {rs.getString("flightcode"), rs.getString("destination"), rs.getString("takeoff"),};
        dt.addRow(o);
    }
    con.close();  // It's good practice to close the connection
    } catch (Exception e) {
    JOptionPane.showMessageDialog(this, e);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

