    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ams", "root", "");

        // Check if the flight code exists in the 'flights' table
        String checkFlightCodeSql = "SELECT COUNT(*) FROM manageflight WHERE FlightCode = ?";
        PreparedStatement checkStmt = con.prepareStatement(checkFlightCodeSql);
        checkStmt.setString(1, FlightCode.getText());  // Assuming 'fc' is a JTextField for flight code
        ResultSet rs = checkStmt.executeQuery();

        int count = 0;
        if (rs.next()) {
            count = rs.getInt(1);
        }

        if (count > 0) {
            // Flight code exists, proceed with insertion
            String sql = "INSERT INTO managepassenger VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ptst = con.prepareStatement(sql);
            ptst.setString(1, name1.getText());  // pn, s, g, a, ph assumed to be text fields for passenger details
            ptst.setString(2, surname.getText());
            ptst.setString(3, gender.getText());
            ptst.setString(4, address.getText());
            ptst.setString(5, Phone.getText());
            ptst.setString(6, FlightCode.getText());
            ptst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Flight Booked!");
        } else {
            // Flight code does not exist, handle accordingly
            JOptionPane.showMessageDialog(this, "Invalid flight code. No flight booked.");
        }

                    con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

